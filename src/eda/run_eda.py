def savefig(fig, name):
    FIG_DIR.mkdir(parents=True, exist_ok=True)
    path = FIG_DIR / f"{name}.png"
    fig.savefig(path, bbox_inches="tight")
    plt.close(fig)
    return path.relative_to(OUTPUT_DIR)


def save_table(df, name):
    TABLE_DIR.mkdir(parents=True, exist_ok=True)
    path = TABLE_DIR / f"{name}.csv"
    df = df.copy()
    float_cols = df.select_dtypes(include=["float64", "float32", "Float64"]).columns
    df[float_cols] = df[float_cols].round(4)
    df.to_csv(path, index=False)
    return path.relative_to(OUTPUT_DIR)


def weighted_std(values, weights):
    """Volume-weighted standard deviation.

    Used to compare how much bad-review rates vary across groupings, such as
    seller vs. category. Weighting by order count avoids overreacting to tiny
    groups with unstable rates.
    """
    avg = np.average(values, weights=weights)
    return np.sqrt(np.average((values - avg) ** 2, weights=weights))


def grouped_bad_review_rate(df, group_col, min_orders=30):
    """Bad-review rate by group, keeping only groups with enough orders.

    This is used for simple categorical cuts such as category, state, and city.
    Seller and product sections use richer helper tables from loader.py.
    """
    sub = df.dropna(subset=[group_col, "bad_review"])
    return (
        sub.groupby(group_col)
        .agg(n_orders=("order_id", "count"), bad_review_rate=("bad_review", "mean"))
        .query("n_orders >= @min_orders")
        .sort_values("bad_review_rate", ascending=False)
    )


class Report:
    """Collects Markdown lines and writes the report at the end."""
    def __init__(self):
        self.lines = ["# EDA Summary\n", "Auto-generated by `src/eda/run_eda.py`. Do not hand-edit.\n"]

    def h2(self, text):
        self.lines.append(f"\n## {text}\n")

    def p(self, text):
        self.lines.append(f"{text}\n")

    def table_ref(self, caption, rel_path):
        self.lines.append(f"\n*{caption}* -> `{rel_path}`\n")

    def fig_ref(self, caption, rel_path):
        self.lines.append(f"\n*{caption}* -> `{rel_path}`\n")

    def write(self, path):
        path.write_text("\n".join(self.lines))


